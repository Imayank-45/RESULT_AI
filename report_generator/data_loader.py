"""
data_loader.py
--------------
Loads result data from multiple sources (Excel files, in-memory records)
and normalizes everything into a unified ReportDataset for analysis.
"""

from __future__ import annotations

import os
import re
from dataclasses import dataclass, field
from datetime import datetime
from typing import Optional, List, Dict, Any

from openpyxl import load_workbook

from report_generator.config import BRANCH_CODES, ReportConfig


# ═══════════════════════════════════════════════════════════════════════
#  DATA MODELS
# ═══════════════════════════════════════════════════════════════════════

@dataclass
class StudentRecord:
    """Normalized student result record."""
    enrollment: str = ""
    name: str = ""
    branch: str = ""               # e.g. "IT", "CS"
    branch_full: str = ""          # e.g. "Information Technology"
    semester: str = ""
    program: str = ""
    subjects: Dict[str, str] = field(default_factory=dict)   # {code: grade}
    sgpa: Optional[float] = None
    cgpa: Optional[float] = None
    result: str = "UNKNOWN"        # PASS / FAIL / UNKNOWN
    fail_subjects: List[str] = field(default_factory=list)
    not_found: bool = False


@dataclass
class ReportDataset:
    """Complete dataset for report generation."""
    records: List[StudentRecord] = field(default_factory=list)
    comparison_records: Optional[List[StudentRecord]] = None
    job_id: str = ""
    generated_date: str = ""
    college_name: str = ""
    department: str = ""
    semester: str = ""
    program: str = ""
    prefix: str = ""
    source_file: str = ""


# ═══════════════════════════════════════════════════════════════════════
#  BRANCH EXTRACTION
# ═══════════════════════════════════════════════════════════════════════

def extract_branch_from_enrollment(enrollment: str) -> tuple[str, str]:
    """
    Extract branch code from RGPV enrollment number.
    Format: CCCCBBYYSS where B = branch letters.
    Examples:
        0105IT2410 → IT, Information Technology
        0105CS2310 → CS, Computer Science & Engineering
        0145PY2411 → PY, Physics (Common)
    """
    # Match 2-letter branch code after 4-digit college code
    match = re.match(r'^\d{4}([A-Z]{2})', enrollment.upper())
    if match:
        code = match.group(1)
        full = BRANCH_CODES.get(code, code)
        return code, full
    return "XX", "Unknown"


# ═══════════════════════════════════════════════════════════════════════
#  LOAD FROM EXCEL
# ═══════════════════════════════════════════════════════════════════════

def load_from_excel(file_path: str, config: ReportConfig) -> ReportDataset:
    """
    Load student records from an Excel file generated by excel_writer.py.

    Expected columns:
        S.NO | ENROLLMENT | NAME | <subject codes...> | RESULT | SGPA | CGPA
    """
    if not os.path.exists(file_path):
        raise FileNotFoundError(f"Excel file not found: {file_path}")

    wb = load_workbook(file_path, read_only=True, data_only=True)
    ws = wb.active

    rows = list(ws.iter_rows(values_only=True))
    if not rows:
        raise ValueError("Excel file is empty")

    headers = [str(h).strip() if h else "" for h in rows[0]]

    # Identify column indices
    enrollment_idx = _find_col(headers, ["ENROLLMENT", "ENROLL", "ENROLLMENT NO"])
    name_idx = _find_col(headers, ["NAME", "STUDENT NAME"])
    result_idx = _find_col(headers, ["RESULT", "STATUS"])
    sgpa_idx = _find_col(headers, ["SGPA"])
    cgpa_idx = _find_col(headers, ["CGPA"])

    # Subject columns are everything between NAME and RESULT
    subject_start = (name_idx + 1) if name_idx is not None else 3
    subject_end = result_idx if result_idx is not None else len(headers) - 3
    subject_codes = headers[subject_start:subject_end]

    records: List[StudentRecord] = []
    prefix = ""

    for row in rows[1:]:
        if not row or all(cell is None for cell in row):
            continue

        enrollment = str(row[enrollment_idx]).strip() if enrollment_idx is not None and row[enrollment_idx] else ""
        if not enrollment:
            continue

        # Extract prefix from first valid enrollment
        if not prefix and len(enrollment) >= 6:
            prefix = enrollment[:10] if len(enrollment) >= 10 else enrollment[:6]

        name = str(row[name_idx]).strip() if name_idx is not None and row[name_idx] else ""

        # Parse subjects
        subjects = {}
        for i, code in enumerate(subject_codes):
            col_idx = subject_start + i
            if col_idx < len(row) and row[col_idx]:
                grade = str(row[col_idx]).strip()
                if grade:
                    subjects[code] = grade

        # Parse SGPA/CGPA
        sgpa = _parse_float(row[sgpa_idx]) if sgpa_idx is not None and sgpa_idx < len(row) else None
        cgpa = _parse_float(row[cgpa_idx]) if cgpa_idx is not None and cgpa_idx < len(row) else None

        # Parse result
        result_raw = str(row[result_idx]).strip().upper() if result_idx is not None and result_idx < len(row) and row[result_idx] else "UNKNOWN"
        if "PASS" in result_raw:
            result = "PASS"
        elif "FAIL" in result_raw:
            result = "FAIL"
        else:
            result = "UNKNOWN"

        # Identify failed subjects
        fail_subjects = [
            code for code, grade in subjects.items()
            if grade.strip().upper() in {"F", "FAIL", "AB", "ABSENT", "RL"}
        ]

        # Extract branch
        branch_code, branch_full = extract_branch_from_enrollment(enrollment)

        record = StudentRecord(
            enrollment=enrollment,
            name=name,
            branch=branch_code,
            branch_full=branch_full,
            semester=config.semester,
            program=config.program,
            subjects=subjects,
            sgpa=sgpa,
            cgpa=cgpa,
            result=result,
            fail_subjects=fail_subjects,
            not_found=False,
        )
        records.append(record)

    wb.close()

    # Generate job ID from filename
    base = os.path.basename(file_path).replace(".xlsx", "")
    job_id = f"JOB-{base}-{datetime.now().strftime('%Y%m%d%H%M%S')}"

    return ReportDataset(
        records=records,
        job_id=job_id,
        generated_date=datetime.now().strftime("%B %d, %Y at %I:%M %p"),
        college_name=config.college_name,
        department=config.department,
        semester=config.semester,
        program=config.program,
        prefix=prefix,
        source_file=file_path,
    )


# ═══════════════════════════════════════════════════════════════════════
#  LOAD FROM IN-MEMORY RECORDS
# ═══════════════════════════════════════════════════════════════════════

def load_from_records(records_list: List[Dict[str, Any]], config: ReportConfig) -> ReportDataset:
    """
    Load from in-memory records (the same format used by ScrapeJobRunner.records).
    Each record dict:
        { "enrollment": str, "name": str, "subjects": {code: grade},
          "sgpa": str, "cgpa": str, "result": "PASS"|"FAIL"|"UNKNOWN",
          "fail_subjects": [str], "not_found": bool }
    """
    records: List[StudentRecord] = []
    prefix = ""

    for rec in records_list:
        if rec.get("not_found"):
            continue

        enrollment = rec.get("enrollment", "")
        if not prefix and len(enrollment) >= 6:
            prefix = enrollment[:10] if len(enrollment) >= 10 else enrollment[:6]

        branch_code, branch_full = extract_branch_from_enrollment(enrollment)

        sgpa = _parse_float(rec.get("sgpa"))
        cgpa = _parse_float(rec.get("cgpa"))

        record = StudentRecord(
            enrollment=enrollment,
            name=rec.get("name", ""),
            branch=branch_code,
            branch_full=branch_full,
            semester=config.semester,
            program=config.program,
            subjects=rec.get("subjects", {}),
            sgpa=sgpa,
            cgpa=cgpa,
            result=rec.get("result", "UNKNOWN"),
            fail_subjects=rec.get("fail_subjects", []),
            not_found=False,
        )
        records.append(record)

    job_id = f"JOB-{datetime.now().strftime('%Y%m%d%H%M%S')}"

    return ReportDataset(
        records=records,
        job_id=job_id,
        generated_date=datetime.now().strftime("%B %d, %Y at %I:%M %p"),
        college_name=config.college_name,
        department=config.department,
        semester=config.semester,
        program=config.program,
        prefix=prefix,
    )


# ═══════════════════════════════════════════════════════════════════════
#  HELPERS
# ═══════════════════════════════════════════════════════════════════════

def _find_col(headers: list, candidates: list) -> Optional[int]:
    """Find column index by checking multiple candidate header names."""
    for i, h in enumerate(headers):
        for c in candidates:
            if h.upper() == c.upper():
                return i
    return None


def _parse_float(value) -> Optional[float]:
    """Safely parse a float from any value."""
    if value is None:
        return None
    try:
        return float(str(value).strip())
    except (ValueError, TypeError):
        return None
